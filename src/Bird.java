public class Bird {
    double wt;
    String color;

    public Bird(double wt, String color) {
        this.wt = wt;
        this.color = color;
    }

    public double getWt() {
        return wt;
    }

    public void setWt(double wt) {
        this.wt = wt;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
